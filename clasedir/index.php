<h1>Evai, Recursos, Clases directo</h1>
<h2 style=''>Clase en directo a través de Google+ y Youtube</h2>

<h2>⚠️ Esta ayuda corresponde a una versión antigua (Hangouts está obsoleto desde noviembre de 2022).</h2>

<ul>
<li>Se trata de llegar a una pantalla similar a esta en Evai:</li><p></p>
<a href='imag/imag1.png' target='_blank'><img src='imag/imag1.png' style='width:600px'></a>


<li>Se ha de tener cuenta en Google+ y Youtube</li><p></p>
<a href='imag/imag2.png' target='_blank'><img src='imag/imag2.png' style='width:800px'></a>

<li>Dar un nombre al hangout y marcar la casilla "Habilitar hangouts en directo"</li><p></p>
<a href='imag/imag3.png' target='_blank'><img src='imag/imag3.png' style='width:800px'></a>

<li>Permitir mostrar el vídeo en Youtube y finalmente pulsar el botón ENTRAR</li><p></p>
<a href='imag/imag4.png' target='_blank'><img src='imag/imag4.png' style='width:800px'></a>

<li>Sale la siguiente pantalla, en la que pulsaremos en "Iniciar emisión"</li><p></p>
<a href='imag/imag5.png' target='_blank'><img src='imag/imag5.png' style='width:800px'></a>

<li>Sale la siguiente pantalla, en la que pulsaremos en "Aceptar"</li><p></p>
<a href='imag/imag6.png' target='_blank'><img src='imag/imag6.png' style='width:800px'></a>

<li>Sale la siguiente pantalla, indica con un temporizador que va a comenzar la emisión en directo</li><p></p>
<a href='imag/imag7.png' target='_blank'><img src='imag/imag7.png' style='width:800px'></a>

<li>La siguiente pantalla indica que ya se está en línea, emitiendo por google+ y youtube. Una opción interesante que se ve en esa imagen es "Compartir pantalla" </li><p></p>
<a href='imag/imag8.png' target='_blank'><img src='imag/imag8.png' style='width:800px'></a>

<li>Para mostrar el vídeo en la pantalla de "Clase directo" del EVAI necesitamos copiar la dirección del vídeo, pulsamos en "Insertar", que está justo a la izquierda de "Finalizar emisión"</li><p></p>
<a href='imag/imag8.png' target='_blank'><img src='imag/imag8.png' style='width:800px'></a>
<p></p>
<li>Y copiamos la dirección del enlace remarcada en la siguiente imagen</li><p></p>
<a href='imag/imag9.png' target='_blank'><img src='imag/imag9.png' style='width:800px'></a>

<hr>

<li>En EVAI, vamos a "Clase directo" y pulsamos en: "Profesor: Ampliar/Reducir" y luego en "Añade o cambia el widget..."</li><p></p>
<a href='imag/imag10.png' target='_blank'><img src='imag/imag10.png' style='width:800px'></a>

<li>Estamos en esta pantalla</li><p></p>
<a href='imag/imag11.png' target='_blank'><img src='imag/imag11.png' style='width:800px'></a>

<li>En el recuadro "pegamos" la dirección que teníamos guardada de antes en Google+ y pulsamos validar</li><p></p>
<a href='imag/imag12.png' target='_blank'><img src='imag/imag12.png' style='width:800px'></a>

<li>Volvemos a la pantalla anterior y <span style='color:red'>pulsamos de nuevo en "Clase directo"</span>, ahora vemos nuestra emisión en directo en EVAI</li><p></p>
<a href='imag/imag13.png' target='_blank'><img src='imag/imag13.png' style='width:800px'></a>

<hr>

<li>Si queremos mostrar imágenes a quien está visualizando esta página, previamente hay que subirlas a la carpeta "clasedirecto" del evai</li><p></p>
<a href='imag/imag14.png' target='_blank'><img src='imag/imag14.png' style='width:800px'></a>

<li>Carpeta claseactual, subir imágenes y cerrar</li><p></p>
<a href='imag/imag15.png' target='_blank'><img src='imag/imag15.png' style='width:800px'></a>

<li>Volvemos a Recursos, Clase directo. Vemos que las imágenes que hemos subido a la carpeta claseactual están ahí en el recuadro gris. Si se pincha cualquiera será mostrada a los usuarios presentes.
Para que no se vean imágenes pulsar en "finalizar presentación si la hay"</li><p></p>
<a href='imag/imag16.png' target='_blank'><img src='imag/imag16.png' style='width:800px'></a>

<li>Pinchando en Antartica.jpg...</li><p></p>
<a href='imag/imag17.png' target='_blank'><img src='imag/imag17.png' style='width:800px'></a>

<li>Al terminar la clase volvemos a Google+ a la pantalla siguiente y pulsamos "Finalizar emisión"</li><p></p>
<a href='imag/imag8.png' target='_blank'><img src='imag/imag8.png' style='width:800px'></a>

<li>Nos saldrá la siguiente pantalla, que nos avisa de que el vídeo de la clase será subido a nuestro canal de Youtube donde podremos gestionarlo (editar, borrar, ocultar, etc)</li><p></p>
<a href='imag/imag18.png' target='_blank'><img src='imag/imag18.png' style='width:800px'></a>

</ul>

